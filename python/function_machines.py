"""From function machines to functions."""


def add_one(num):
    """Adds 1 to a number."""
    return num + 1


def add_two(num):
    """Adds 2 to a number."""
    return num + 2


def square(num):
    """Squares a number."""
    return num * num


def cube(num):
    """Cubes a number."""
    return num * num * num

